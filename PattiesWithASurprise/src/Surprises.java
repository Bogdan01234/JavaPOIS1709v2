public class Surprises {

    public static String getSurprises(int patty) {

        String surprise;
        switch (patty){
            case 0: surprise = "Surprise MF";
                return surprise;
            case 1: surprise = "Bingo";
                return surprise;
            case 2: surprise = "Loooooser";
                return surprise;
            case 3: surprise = "Anything";
                return surprise;
            default: surprise = "NULL";
                return surprise;
        }
    }
}
